package db222pt_assign3;



public class Moon {
	//Variabler
	private String name = "";
	private int diameter = 0;



	// Getters och setters
	public void setName(String theName) {
		name = theName;
	}
	public String getName() {
		return name;
	}
	public void setDiameter(int theDiameter) {
		diameter = theDiameter;
	}

	public int getDiameter() {
		return diameter;
	}

}
